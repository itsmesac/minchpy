package minchpy.entity;

public class Evaluation {
    double evalValue;
    
    public Evaluation() {
        super();
    }

    public Evaluation(double evalValue) {
        super();
        this.evalValue = evalValue;
    }

    public double getEvalValue() {
        return evalValue;
    }

    public void setEvalValue(double evalValue) {
        this.evalValue = evalValue;
    }
    
}
